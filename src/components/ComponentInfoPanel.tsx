export default function ComponentInfoPanel({
  componentType,
  customDescription,
  onDescriptionChange,
  onClose,
  onCompare,
  label
}: ComponentInfoPanelProps) {
  const [isEditing, setIsEditing] = useState(false)
  const [localDescription, setLocalDescription] = useState(customDescription || '')

  // Приоритизируем описание по label, если оно есть
  let info = (label && labelDescriptions[label]) || componentDescriptions[componentType]

  // Если все еще нет, и это облачный сервис, создаем динамическое описание
  if (!info && label) {
    if (label.startsWith('AWS ')) {
      info = {
        title: label,
        description: `Сервис Amazon Web Services (${label}). Обеспечивает облачные возможности для вашей архитектуры.`,
        useCases: ['Облачная инфраструктура AWS', 'Масштабируемость и надежность']
      }
    } else if (label.startsWith('Azure ')) {
      info = {
        title: label,
        description: `Сервис Microsoft Azure (${label}). Предоставляет облачные решения в экосистеме Microsoft.`,
        useCases: ['Облачные вычисления Azure', 'Интеграция с сервисами Microsoft']
      }
    } else if (label.startsWith('OCI ') || label.startsWith('Oracle ')) {
      info = {
        title: label,
        description: `Сервис Oracle Cloud Infrastructure (${label}). Оптимизирован для корпоративных нагрузок и баз данных.`,
        useCases: ['Oracle Cloud', 'Enterprise инфраструктура']
      }
    } else if (label.startsWith('GCP ') || label.startsWith('Google Cloud ')) {
      info = {
        title: label,
        description: `Сервис Google Cloud Platform (${label}). Предоставляет инновационные облачные технологии Google.`,
        useCases: ['Google Cloud', 'Масштабируемые облачные решения']
      }
    }
  }

  useEffect(() => {
    setLocalDescription(customDescription || '')
  }, [customDescription])



  // Fallback title if info is missing
  const title = info?.title || componentType.charAt(0).toUpperCase() + componentType.slice(1).replace(/-/g, ' ')

  return (
    <div
      style={{
        position: 'absolute',
        top: '50%',
        left: '50%',
        transform: 'translate(-50%, -50%)',
        backgroundColor: '#2d2d2d',
        border: '2px solid #4dabf7',
        borderRadius: '16px',
        padding: '30px',
        minWidth: '500px',
        maxWidth: '700px',
        maxHeight: '85vh',
        overflowY: 'auto',
        boxShadow: '0 12px 48px rgba(0,0,0,0.7)',
        zIndex: 2000,
      }}
    >
      <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '20px' }}>
        <h2 style={{ fontSize: '22px', fontWeight: 'bold', color: '#fff', margin: 0 }}>
          {title}
        </h2>
        <div style={{ display: 'flex', gap: '10px' }}>
          {onCompare && (
            <button
              onClick={() => onCompare(componentType)}
              style={{
                background: '#339af0',
                border: 'none',
                color: '#fff',
                padding: '8px 16px',
                borderRadius: '6px',
                cursor: 'pointer',
                fontWeight: 600,
                fontSize: '14px'
              }}
            >
              Сравнить
            </button>
          )}
          <button
            onClick={onClose}
            style={{
              background: 'transparent',
              border: 'none',
              color: '#aaa',
              fontSize: '28px',
              cursor: 'pointer',
              padding: '0',
              width: '32px',
              height: '32px',
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'center',
              borderRadius: '6px',
              transition: 'all 0.2s',
            }}
            onMouseEnter={(e) => {
              e.currentTarget.style.color = '#fff'
              e.currentTarget.style.backgroundColor = '#3d3d3d'
            }}
            onMouseLeave={(e) => {
              e.currentTarget.style.color = '#aaa'
              e.currentTarget.style.backgroundColor = 'transparent'
            }}
          >
            <X size={24} />
          </button>
        </div>
      </div>

      {onDescriptionChange && (
        <div style={{ marginBottom: '24px', background: '#383838', padding: '16px', borderRadius: '8px', border: '1px solid #555' }}>
          <div style={{ display: 'flex', justifyContent: 'space-between', marginBottom: '8px', alignItems: 'center' }}>
            <h3 style={{ fontSize: '16px', color: '#4dabf7', margin: 0 }}>Описание этого компонента</h3>
            {!isEditing ? (
              <button
                onClick={() => setIsEditing(true)}
                style={{ background: 'transparent', border: 'none', color: '#aaa', cursor: 'pointer', display: 'flex', gap: '6px', alignItems: 'center', fontSize: '14px' }}
              >
                <Edit3 size={16} /> Редактировать
              </button>
            ) : (
              <button
                onClick={() => setIsEditing(false)}
                style={{ background: '#4dabf7', border: 'none', color: '#fff', cursor: 'pointer', padding: '4px 8px', borderRadius: '4px', display: 'flex', gap: '6px', alignItems: 'center', fontSize: '14px' }}
              >
                Готово
              </button>
            )}
          </div>

          {isEditing ? (
            <textarea
              value={localDescription}
              onChange={(e) => {
                const newValue = e.target.value
                setLocalDescription(newValue)
                if (onDescriptionChange) {
                  onDescriptionChange(newValue)
                }
              }}
              placeholder="Добавьте описание для этого конкретного компонента..."
              style={{
                width: '100%',
                minHeight: '80px',
                background: '#222',
                color: '#eee',
                border: '1px solid #555',
                borderRadius: '4px',
                padding: '8px',
                fontSize: '14px',
                fontFamily: 'inherit',
                resize: 'vertical'
              }}
            />
          ) : (
            <p style={{ color: localDescription ? '#eee' : '#888', fontStyle: localDescription ? 'normal' : 'italic', whiteSpace: 'pre-wrap', lineHeight: '1.5', margin: 0, fontSize: '14px' }}>
              {localDescription || 'Описание не добавлено. Нажмите "Редактировать", чтобы добавить.'}
            </p>
          )}
        </div>
      )}

      {info ? (
        <>
          <div style={{ marginBottom: '20px' }}>
            <h3 style={{ fontSize: '18px', color: '#4dabf7', marginBottom: '10px' }}>Справка по типу</h3>
            <p style={{ color: '#ccc', lineHeight: '1.6', fontSize: '16px', margin: 0 }}>
              {info.description}
            </p>
          </div>

          <div>
            <h3 style={{ fontSize: '18px', color: '#4dabf7', marginBottom: '10px' }}>Примеры использования:</h3>
            <ul style={{ paddingLeft: '20px', color: '#ccc', margin: 0 }}>
              {info.useCases.map((useCase: string, index: number) => (
                <li key={index} style={{ marginBottom: '8px', fontSize: '15px', lineHeight: '1.4' }}>
                  {useCase}
                </li>
              ))}
            </ul>
          </div>
        </>
      ) : (
        <div style={{ marginTop: '20px', paddingTop: '20px', borderTop: '1px solid #444' }}>
          <p style={{ color: '#888', fontSize: '14px' }}>
            Справочная информация для этого типа компонентов пока отсутствует.
          </p>
        </div>
      )}
    </div>
  )
}


